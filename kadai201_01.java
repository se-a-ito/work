public class kadai201_01 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		/*型表にあるすべての型に対応する変数に値を入れて標準出力する。 ※pythonの方は各変数の変数型も出力する

			ひとつのプログラムで以下のすべてを出力すること
			文字列は「'」や「"」も含めて出力すること
		*/
		
		int integer = 100;
		double decimal = 100.12;
		String string1 = "'100'";
		String string2 = "\"100\"";
		String string3 = "ヤッホー";
		boolean ok = true;
		boolean bad = false;
		System.out.println(integer); 
		System.out.println(decimal);
		System.out.println(string1);
		System.out.println(string2);
		System.out.println(string3);
		System.out.println(ok);
		System.out.println(bad);
	}

}
